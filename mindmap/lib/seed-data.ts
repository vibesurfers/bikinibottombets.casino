export type SeedSet = 'yc' | 'thiel' | 'vc' | 'ai' | 'both' | 'all';

export function getSeedActors(seedSet: SeedSet): SeedActorInput[] {
  switch (seedSet) {
    case 'yc': return ycSeedActors;
    case 'thiel': return thielSeedActors;
    case 'vc': return vcSeedActors;
    case 'ai': return aiSeedActors;
    case 'both': return [...ycActors, ...thielActors];
    case 'all': return allSeedActors;
  }
}

export function getSeedConnections(seedSet: SeedSet): SeedConnectionInput[] {
  const actors = getSeedActors(seedSet);
  const slugs = new Set(actors.map(a => a.slug));
  return seedConnections.filter(c => slugs.has(c.sourceSlug) && slugs.has(c.targetSlug));
}
